        <h2 class="padded" id="our-faith">
            Our Faith
        </h2>

        <div class="general-text">

            <p>Methodists have always been clear that no-one is beyond the reach of God's love.</p>

            <p>The calling of the Methodist Church is to respond to the gospel of God's love in Christ and to live out its discipleship in
            worship and mission.</p>

            <p>A member of the Methodist Church is called to:</p>
			<ul>
            	<li>worship within the local church, including regular sharing in Holy Communion, and through personal prayer</li>
				<li>learning and caring, through Bible study and meeting for fellowship, so that they may grow in faith and support
				others in their discipleship</li>
				<li>service, by being a good neighbour in the community, challenging injustice and using their resources to support
				the Church in its mission in the world</li>
				<li>evangelism, through working out their faith in daily life and sharing Christ with others.</li>
			</ul>
		</div>

		<h2 class="padded-top">Four Alls</h2>
		<div class="general-text">
        	<p>The traditional summary of Methodist teaching is:</p>
			<ul>
            	<li>All need to be saved.</li>

                <li>All may be saved.</li>

                <li>All may know themselves saved.</li>

                <li>All may be saved to the uttermost.</li>
			</ul>


         	<p>More information about our faith can be found on the <a href="https://www.methodist.org.uk/our-faith/">Methodist Church of
         	Great Britain website</a>.</p>

