<div class="row">
    <div class="col-md-12">
        <div>
            <p class="general-text padded-top">By the early 1970s, the decision was taken to demolish this church, at the same time as the old Queens Theatre next door,
            and re-develop the whole site.</p>

            <p class="general-text"><span class="bold">1975</span> - The new church premises, largely above shops in the centre of Blackpool, were opened on Saturday 15th March 1975 by
            Miss Charlotte Briggs, the oldest member of the Society at that time and dedicated by the then Chairman of the District, the Rev. G. Herbert Raynor.</p>

			<p class="general-text"><span class="bold">1997</span> - when the congregation at Chapel Street joined with the Central Church, it was re-named
			“New Central Methodist Church”.</p>
