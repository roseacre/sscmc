		<h2 class="padded-top">
			History of Central (Adelaide Street) (Bank Hey Street) Methodist Church
		</h2>

        <div>
            <p class="general-text padded-top"><span class="bold">1835</span> – the first Wesleyan chapel, able to accommodate 250 to 300 worshippers, was erected in Adelaide Street.
            In 1852, when more school accommodation was required, a larger school was erected facing into Bank Hey Street.</p>

			<p class="general-text"><span class="bold">1848</span> - Slater’s Directory (Lancashire), page 25, the Places of Worship section includes:
             'Independent Chapel, Bonny’s Lane, (ministers various)' and 'Wesleyan Chapel, Bank Hey St (ministers various)'.</p>
        </div>
    </div>
</div>

