			<p class="general-text"><span class="bold">2017</span> - joined with Lindale Methodist Church and Waterloo Road Methodist Church to
			form South Shore Methodist Community Church.</p>
			<p class="general-text">The church is included on a Blackpool Council List of buildings of
			historical significance.</p>

