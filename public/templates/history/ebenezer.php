<div class="row">
	<div class="col-md-12">
        <p class="general-text padded-top"><span class="bold">1869</span> – Ebenezer (Rawcliffe Street) Wesleyan Methodist Church was founded. Ebenezer Wesleyan Society actually
        began meeting in the Temperance Hall, between Bolton Street and South Parade. However, in 1869, they purchased land in Rawcliffe St and built
        the Rawcliffe Street Ebenezer Wesleyan Chapel, which opened on 2 September 1869. The building could seat 350 worshippers. The architects
        were Thomas Bird and Sons of Manchester, and the builder was RW Braithwaite of Blackpool. </p>

        <p class="general-text"><span class="bold">1870</span> - a Sunday school for 250 children opened next door to the church.</p>

        <p class="general-text"><span class="bold">1888</span> - Building of a new Rawcliffe Street Wesleyan chapel started on the corner of Moore Street, next to the
        old church.</p>

        <p class="general-text"><span class="bold">1889</span> - The new church opened on 14 June 1889 and the old church was then used as a school.</p>

        <p class="general-text"><span class="bold">1895</span> - Kelly’s Directory (Lancashire), page 107, details
        'The Wesleyan chapel, Rawcliffe Street, South Shore, opened in 1889, in a mixed Gothic style; the tower is not yet completed;
        the cost, including the organ was £5,050, and there is accommodation for 700 persons; there is a memorial window to the late
        Francis Parnell in the chancel at the West end; there are two other memorial windows, one given by Thomas Ward, the other by
        William Kenyon in memory of his mother.'</p>

		<p class="general-text"><span class="bold">1924</span> - Kelly’s Directory (Lancashire), page 194, details
		'The Wesleyan Methodist Chapel, Rawcliffe Steet, South Shore, opened in 1889, is in the Gothic style; there are memorial windows
		to Francis Parnell and others, and sittings for 700 persons.'</p>

        <p class="general-text"><span class="bold">1971</span> – Rawcliffe Street Methodist Church was closed on 25 July 1971, and demolished in 1972. The church had been
        replaced by Clarence Court flats by 1974.</p>

		<p class="general-text">Four stained glass windows from the Church were moved into the South Nave of Holy Trinity Church.
		They depict the four Evangelists (Matthew, Mark, Luke, and John).</p>

		<p class="general-text">A memorial plaque, commemorating parishioners lost in World War 1, was discovered in 2008 – in a scrap yard
		in Kirby Lonsdale. It is currently located in Highfield Methodist Church.</p>
