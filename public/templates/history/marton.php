		<h2 class="padded-top">
			History of Marton Methodist Church
		</h2>

        <div>
            <p class="general-text padded-top"><span class="bold">1810</span> - A Methodist Society was formed in Marton.</p>

            <p class="general-text"><span class="bold">1895</span> - Kelly’s Directory (Lancashire), page 110, makes reference to
            'a small Wesleyan chapel at Marton Moss Side, erected in 1872 at a cost of £300 and seating 150 persons.'</p>
            <p class="general-text">Services at the Methodist Chapel, Little Marton, were held at 2:30pm on Sundays.</p>
            <p class="general-text">The chapel is also included in the 1924 Kelly's Directory.</p>

            <p class="general-text"><span class="bold">1958</span> - Marton Methodist Church Hall and Sunday School were opened and dedicated on 10 May 1958, at
            Walker’s Hill on Midgeland Road. It comprised a hall, lecture room and three classrooms.</p>
            <p class="general-text"><span class="bold">1975</span> - the current Marton Methodist Church was opened next door to the church hall in September 1975.</p>
        </div>

    </div>

</div>

<div class="row">
    <div class="col-md-12">
    	<p class="general-text padded-top">There's a black granite memorial tablet set into the wall of Marton Methodist Church, with details on
    	<a href="https://www.warmemorialsonline.org.uk/memorial/51371/">warmemorialsonline.org.uk</a></p>
		<p class="general-text">The memorial was moved when the original Methodist church closed and a new one was built.</p>

    	<p class="general-text">The inscription reads: ‘This tablet was erected by subscription by the inhabitants of Marton to the
    	perpetual memory of those brave men from this parish who fell in the European War 1914 – 1918’</p>
