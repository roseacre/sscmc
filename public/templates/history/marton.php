            <p class="general-text padded-top">1810 - A Methodist Society was formed in Marton.</p>

            <p class="general-text">1895 - Kelly’s Directory (Lancashire), page 110, makes reference to
            'a small Wesleyan chapel at Marton Moss Side, erected in 1872 at a cost of £300 and seating 150 persons.'</p>
            <p class="general-text">Services at the Methodist Chapel, Little Marton, were held at 2:30pm on Sundays.</p>
            <p class="general-text">The chapel is also included in the 1924 Kelly's Directory.</p>

            <p class="general-text">1958 - Marton Methodist Church Hall and Sunday School were opened and dedicated on 10 May 1958, at
            Walker’s Hill on Midgeland Road. It comprised a hall, lecture room and three classrooms.</p>
            <p class="general-text">1975 - the current Marton Methodist Church was opened next door to the church hall in September 1975.</p>
