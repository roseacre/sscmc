        	<p class="general-text padded-top">An Independent Methodist (later Primitive Methodist) Church was established in Chapel Street in 1826.
        	This is recorded as having closed in 1849.</p>
            <p class="general-text">1876 – Chapel Street Primitive Methodist Church was founded.</p>
            <p class="general-text">1997 – the church closed.</p>

            <p class="general-text">The congregation moved on closure in July 1997 to join the congregation at Central Methodist Church,
            Adelaide Street, Blackpool, at which time the joint Church was renamed 'New Central Methodist Church'.</p>

            <p class="general-text">The church building was leased to Manchester Methodist Housing Association (later Great Places Housing)
            and modified to become the Blackpool 'Foyer', with 38 self-contained flatlets to house young people in need of accommodation,
            frequently when coming out of Care. The Foyer closed in 2015 and the lease was bought by Blackpool Housing Association in March 2016.</p>
