                    History of Churches in The Blackpool Methodist Circuit
                </h2>

                <div class="general-text">

                    <p>The Fylde was originally served by Methodist ministers from Preston, but, in 1811, it was included in the
                    new Garstang Wesleyan Circuit. In 1855, the circuit was renamed the Blackpool and Garstang Wesleyan Circuit.</p>

                    <p>In 1866, Blackpool Wesleyan Circuit was created, taking in the Blackpool, Fleetwood, Poulton and Lytham areas, until
                    Lytham formed its own circuit in 1883. Note that the Primitive and United Methodists had their own circuits until 1934.</p>

                    <p>In 1934 (after Methodist Union in 1932), Blackpool was split into three circuits: Fleetwood, Blackpool North
                    and Blackpool South.</p>

                    <p>Currently, the Fylde is served by the Blackpool Methodist Circuit and the North Fylde Methodist Circuit.
                    The Blackpool Methodist Circuit comprises the four churches in South Shore, Marton, Laytopn and Central Blackpool. The
                    <a href="http://www.northfyldemethodistcircuit.org.uk/churches" rel="noopener noreferrer" target="_blank">North Fylde Methodist Circuit</a> comprises seven churches in
                    Pilling, Knott End, Poulton Le Fylde, Thornton Cleveleys, and Fleetwood.</p>

                    <p>The area covered by the present Blackpool Methodist Circuit has been served by a number of churches over the years.
                    We'd love to receive any memories or photographs that you have so we can share them with the community.
